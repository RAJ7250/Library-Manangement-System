    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if(type.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this, "Select a type", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(type.getSelectedIndex() == 1){
            if(!check(age.getText())){
                JOptionPane.showMessageDialog(this, "Input an integer in Age", "Inane error", JOptionPane.ERROR_MESSAGE);
                age.setText("");
                return;
            }
            sys.addSub(new Golden(sys.subid, name.getText(), Integer.parseInt(age.getText())));
        }
        else if (type.getSelectedIndex() == 2) {
            if (!check(age.getText())) {
                JOptionPane.showMessageDialog(this, "Input an integer in Age", "Inane error", JOptionPane.ERROR_MESSAGE);
                age.setText("");
                return;
            }
            sys.addSub(new Normal(sys.subid, name.getText(), Integer.parseInt(age.getText())));
        }
        name.setText("");
        age.setText("");
        id.setText(Integer.toString(sys.subid));
        
    }//GEN-LAST:event_addActionPerformed

