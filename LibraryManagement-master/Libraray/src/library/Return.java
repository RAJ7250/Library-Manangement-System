    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        int ID = 0;
        try {
            ID = Integer.parseInt(searchstu.getText());

            int cursub = sys.searchsub(ID);
            if (cursub == -1) {
                JOptionPane.showMessageDialog(this, "Student not found", "Inane error", JOptionPane.ERROR_MESSAGE);
                searchstu.setText("");
                return;
            }
            subname.setText(sys.sub.get(cursub).name);
            subtype.setText(sys.sub.get(cursub).type);
            if (sys.sub.get(cursub).issued.size() == 0) {
                JOptionPane.showMessageDialog(this, "No book issued", "Inane error", JOptionPane.ERROR_MESSAGE);
                searchstu.setText("");
                return;
            }
            d.setEnabled(true);
            m.setEnabled(true);
            y.setEnabled(true);
            jButton1.setEnabled(true);
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            curbook = 0;
            update();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter Valid ID", "Inane error", JOptionPane.ERROR_MESSAGE);
            searchstu.setText("");
        }
    }//GEN-LAST:event_SearchActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (curbook < 0) {
            JOptionPane.showMessageDialog(this, "No book issued", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        curbook = (curbook + 1) % sys.sub.get(cursub).issued.size();
        update();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (curbook < 0) {
            JOptionPane.showMessageDialog(this, "No book issued", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        curbook = (curbook - 1 + sys.sub.get(cursub).issued.size()) % sys.sub.get(cursub).issued.size();
        update();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!check(d.getText())) {
            JOptionPane.showMessageDialog(this, "Input an integer", "Inane error", JOptionPane.ERROR_MESSAGE);
            d.setText("");
            return;
        }
        if (!check(m.getText())) {
            JOptionPane.showMessageDialog(this, "Input an integer", "Inane error", JOptionPane.ERROR_MESSAGE);
            m.setText("");
            return;
        }
        if (!check(y.getText())) {
            JOptionPane.showMessageDialog(this, "Input an integer", "Inane error", JOptionPane.ERROR_MESSAGE);
            y.setText("");
            return;
        }
        int dd = Integer.parseInt(d.getText());
        int mm = Integer.parseInt(m.getText());
        int yy = Integer.parseInt(y.getText());
        int val = sys.searchconid(sys.sub.get(cursub).issued.get(curbook));
        if (val >= 0) {
            sys.content.get(val).status = 0;
        }
        int st = sys.sub.get(cursub).getfine(new Date(dd, mm, yy), curbook);
        if (st > 0) {
            JOptionPane.showMessageDialog(this, "Fine of Rs: " + st, "Inane error", JOptionPane.ERROR_MESSAGE);
        }
        if (sys.sub.get(cursub).issued.size() == 0) {
            curbook = -1;
            JOptionPane.showMessageDialog(this, "No Book is issued to this subscriber", "Inane error", JOptionPane.ERROR_MESSAGE);
            subname.setText("");
            subtype.setText("");
            d.setText("");
            m.setText("");
            y.setText("");
            d.setEnabled(false);
            m.setEnabled(false);
            y.setEnabled(false);
            jButton1.setEnabled(false);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            searchstu.setText("");
        } else {
            curbook = curbook % sys.sub.get(cursub).issued.size();
            d.setText("");
            m.setText("");
            y.setText("");
            update();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    /**
     * @param args the command line arguments
     */
    public boolean check(String s) {
        for (int i = 0; i < s.length(); ++i) {
            if (s.charAt(i) < '0' || s.charAt(i) > '9') {
                return false;
            }
        }
        return true;
    }

    private void update() {
        int val = sys.searchconid(sys.sub.get(cursub).issued.get(curbook));
        name.setText(sys.content.get(val).name);
        author.setText(sys.content.get(val).author);
        topic.setText(sys.content.get(val).topic);
    }
