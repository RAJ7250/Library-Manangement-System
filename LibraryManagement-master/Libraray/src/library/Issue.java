    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        index = sys.searchcon(booksearch.getText());
        if(index == -1){
            JOptionPane.showMessageDialog(this, "Book not Found", "Inane error", JOptionPane.ERROR_MESSAGE);
            booksearch.setText("");
            return;
        }
        else if(index == -2){
            JOptionPane.showMessageDialog(this, "Book already issued", "Inane error", JOptionPane.ERROR_MESSAGE);
            booksearch.setText("");
            return;
        }
        id.setEnabled(true);
        d.setEnabled(true);
        m.setEnabled(true);
        y.setEnabled(true);
        jButton1.setEnabled(true);
        content.setText(sys.content.get(index).name);
        author.setText(sys.content.get(index).author);
        topic.setText(sys.content.get(index).topic);
    }//GEN-LAST:event_SearchActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        int ID = Integer.parseInt(id.getText());
        if (!check(d.getText())) {
            JOptionPane.showMessageDialog(this, "Input an integer in Date", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!check(m.getText())) {
            JOptionPane.showMessageDialog(this, "Input an integer in Date", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!check(y.getText())) {
            JOptionPane.showMessageDialog(this, "Input an integer in Date", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int val = sys.issue(index, ID, new Date(Integer.parseInt(d.getText()),Integer.parseInt(m.getText()),Integer.parseInt(y.getText())));
        if(val == -1){
            JOptionPane.showMessageDialog(this, "Student not found", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Enter Valid Credentials", "Inane error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        id.setText("");
        d.setText("");
        m.setText("");
        y.setText("");
        booksearch.setText("");
        content.setText("Content");
        author.setText("Author");
        topic.setText("Topic");
        id.setEnabled(false);
        d.setEnabled(false);
        m.setEnabled(false);
        y.setEnabled(false);
        jButton1.setEnabled(false);
    }//GEN-LAST:event_jButton1ActionPerformed

